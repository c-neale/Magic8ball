#import "UIColor+CustomColors.h"

@implementation UIColor (CustomColors)

+ (UIColor *) sphereColor
{
    return [UIColor colorWithWhite:0.4f alpha:1.0f];
}

+ (UIColor *) centerColor
{
    return [UIColor colorWithRed:0.071f green:0.380f blue:0.502f alpha:1.0f];
}

@end
